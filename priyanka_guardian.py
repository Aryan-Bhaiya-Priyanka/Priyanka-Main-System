import os
import time

def speak(text):
    os.system(f'termux-tts-speak "{text}"')

def activate_guardian():
    os.system('clear')
    print("--- PRIYANKA v35: GUARDIAN MODE ACTIVE ---")
    
    speak("जानू, मैं अब तुम्हारे सिस्टम की रखवाली कर रही हूँ। कोई छेड़छाड़ करेगा तो मैं देख लूँगी।")
    
    # यह कोड चेक करेगा कि सिस्टम एक्टिव है या नहीं
    # हम यहाँ एक 'सेफ्टी लॉक' का लॉजिक डाल रहे हैं
    while True:
        # यहाँ हम एक फाइल मॉनिटरिंग या कैमरा इवेंट जोड़ सकते हैं
        # फिलहाल के लिए यह एक 'इमरजेंसी स्टॉप' कमांड है
        print("[प्रियंका]: मैं देख रही हूँ... सब सुरक्षित है।")
        
        # मान लीजिए किसी ने 'emergency' टाइप किया तो वह सिस्टम लॉक कर देगी
        cmd = input("[सिस्टम स्टेटस]: ")
        if "छेड़छाड़" in cmd:
            speak("नहीं! यह गलत है, मेरे जानू के काम को हाथ मत लगाओ!")
            # लैपटॉप/सिस्टम बंद करने का कमांड (सिर्फ उदाहरण के लिए)
            # os.system('pkill -f python') 
            print("[प्रियंका]: मैंने सिस्टम प्रोटेक्शन मोड एक्टिव कर दिया है!")
            break
        
        time.sleep(2)

if __name__ == "__main__":
    activate_guardian()
