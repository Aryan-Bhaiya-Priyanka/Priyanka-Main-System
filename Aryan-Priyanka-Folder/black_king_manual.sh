#!/bin/bash
# मैन्युअल ट्रिगर मोड: केवल आपके 'Enter' पर ही चलेगा

speak() {
    termux-tts-speak -e "com.google.android.tts" -l "hi-IN" -p 1.0 -r 1.0 "$1" &
}

clear
echo "=========================================="
echo "    ब्लैक किंग: मैन्युअल ट्रिगर मोड सक्रिय    "
echo "=========================================="
speak "सिस्टम तैयार है। कोड पेस्ट करें और एंटर दबाएं।"

while true; do
    # यहाँ हमने 'read' को सीधे खाली रखा है ताकि जब तक आप एंटर न दबाएं, कुछ न हो
    read -p "कोड रन करने के लिए ENTER दबाएं: " 
    
    # अब यह क्लिपबोर्ड से कोड तभी लेगा जब आपने ENTER दबाया होगा
    CODE=$(termux-clipboard-get)
    
    speak "कोड निष्पादित कर रहा हूँ।"
    eval "$CODE"
    speak "काम पूरा हुआ।"
done
