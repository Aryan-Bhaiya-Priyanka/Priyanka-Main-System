#!/bin/bash
# सुरक्षित माइग्रेशन: सिर्फ प्रियंका की फाइलें, ब्लैक किंग को टच नहीं करना।

# चेक करें कि आर्यन फोल्डर मौजूद है या नहीं
if [ -d "$HOME/Aryan" ]; then
    echo "[+] आर्यन फोल्डर मिल गया।"
    
    # सिर्फ उन फाइलों को मूव करें जो 'priyanka' से शुरू होती हैं
    # यह ब्लैक किंग की किसी भी फाइल को नहीं छेड़ेगा
    mv ~/*priyanka* ~/Aryan/ 2>/dev/null
    
    echo "[+] प्रियंका की सभी फाइलें सफलतापूर्वक ~/Aryan/ में स्थानांतरित कर दी गई हैं।"
    echo "[!] ब्लैक किंग की फाइलें सुरक्षित और यथावत हैं।"
else
    echo "[!] त्रुटि: ~/Aryan फोल्डर नहीं मिला।"
fi
