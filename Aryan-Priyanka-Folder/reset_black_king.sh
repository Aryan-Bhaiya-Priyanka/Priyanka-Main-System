#!/data/data/com.termux/files/usr/bin/bash

# 1. सभी अटके हुए वॉइस और बैकग्राउंड प्रोसेसेस को बंद करना (सिर्फ रिसेट)
pkill -f termux-tts-speak
pkill -f black_king

# 2. स्क्रीन क्लियर करके सिस्टम को फ्रेश स्टेट में लाना
clear
echo "=========================================="
echo "        [ BLACK KING SYSTEM RESET ]      "
echo "=========================================="
echo "[+] सिस्टम कॉन्फ़िगरेशन रिफ्रेश किया जा रहा है..."

# 3. वॉइस इंजन को फिर से रि-इनिशियलाइज़ करना
(
  termux-tts-speak -e "com.google.android.tts" -l "hi-IN" "सिस्टम रिसेट पूरा हुआ। ब्लैक किंग अब तैयार है।"
) &

echo "[+] वॉइस इंजन सफलतापूर्वक रि-लोड हो गया है।"
echo "[+] डेटा सुरक्षित है। ट्रेनिंग जारी रखने के लिए तैयार।"
